import java.util.*;

public class shift {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        int a =sc.nextInt();
        

        
        System.out.println("Left shift Operation ="+(a<<2));
        
        System.out.println("Right Shift operation ="+(a>>2));
      
        System.out.println("Unsigned Right shift opertaion ="+(a>>>2));


    }
}
